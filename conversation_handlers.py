def _compose_question_answer(state: ConversationState, question: str) -> str:
    """Answer merchant's question directly and re-offer value."""
    merchant = state.merchant_context or {}
    category = state.category_context or {}
    lang = state.detected_language
    name = merchant.get("identity", {}).get("owner_first_name", "")
    q_lower = question.lower()

    if any(w in q_lower for w in ["cost", "price", "kitne", "kitna", "charges", "fees", "amount"]):
        if lang == "hi-en":
            return f"Aapke Pro plan mein yeh service included hai — koi extra charge nahi. {name or 'Aap'} chahein toh main full breakdown bhej sakti hoon. Reply YES?"
        return "Under your current Pro plan, this service is included at no extra charge. Want a full breakdown? Reply YES"

    if any(w in q_lower for w in ["time", "kab", "when", "kitne time", "how long", "kitna time"]):
        if lang == "hi-en":
            return "Google pe typically 24-48 ghante mein reflect hota hai. Main progress track karke update karti hoon. Koi aur sawaal?"
        return "Typically reflects on Google within 24-48 hours. I'll track and update you. Any other questions?"

    perf = merchant.get("performance", {})
    peer_stats = category.get("peer_stats", {})
    if perf.get("ctr") and peer_stats.get("avg_ctr"):
        if lang == "hi-en":
            return (
                f"Main aapka Google profile optimize karti hoon — photos, posts, offers, reviews. "
                f"Aapka CTR {int(perf['ctr']*100)}% hai; peers {int(peer_stats['avg_ctr']*100)}% pe hain. "
                f"Main yeh gap close kar sakti hoon. Chalega? Reply YES"
            )
        return (
            f"I optimize your Google Business Profile — photos, posts, offers, reviews. "
            f"Your CTR is {int(perf['ctr']*100)}% vs peer avg {int(peer_stats['avg_ctr']*100)}%. "
            f"I can close that gap. Want me to start? Reply YES"
        )

    if lang == "hi-en":
        return "Samajh gayi — main check karti hoon. Ek second. Kya aap free profile audit chahenge? (FREE, 2 min) Reply YES"
    return "Got it — let me check on that. Want me to run a quick free profile audit while I'm at it? Reply YES"


def _compose_advance(state: ConversationState, merchant_message: str) -> str:
    """Advance with a new angle when merchant is neutral."""
    merchant = state.merchant_context or {}
    category = state.category_context or {}
    lang = state.detected_language
    name = merchant.get("identity", {}).get("owner_first_name", "")
    turn = state.turn_count
    perf = merchant.get("performance", {})
    customer_agg = merchant.get("customer_aggregate", {})
    peer_stats = category.get("peer_stats", {})

    if turn <= 2:
        lapsed = customer_agg.get("lapsed_180d_plus", 0)
        if lapsed > 5:
            if lang == "hi-en":
                return (
                    f"{name or 'Aap'}, aapke {lapsed} customers 6+ months se nahi aaye. "
                    f"Main unhe recall message bhej sakti hoon aapki taraf se — typically 20-30% wapas aate hain. "
                    f"Try karein? Reply YES / STOP"
                )
            return (
                f"{name or 'Quick note'}: {lapsed} of your customers haven't returned in 6+ months. "
                f"I can send them a recall message — typically 20-30% come back. Worth trying? Reply YES / STOP"
            )

    if turn == 3:
        views = perf.get("views", 0)
        ctr = perf.get("ctr", 0)
        peer_ctr = peer_stats.get("avg_ctr", 0.03)
        if views and ctr and ctr < peer_ctr:
            missed = int(views * (peer_ctr - ctr))
            if lang == "hi-en":
                return (
                    f"Last 30 days mein ~{missed} profile visitors convert nahi hue (CTR gap). "
                    f"Main wajah diagnose karke fix kar sakti hoon — 10 min ka kaam. Reply YES?"
                )
            return (
                f"In the last 30 days, ~{missed} profile visitors didn't convert (CTR gap vs peers). "
                f"I can diagnose and fix the reason in 10 minutes. Reply YES?"
            )

    if turn == 4:
        if lang == "hi-en":
            return f"Ek quick question {name or ''}: is mahine kaunsi service ke liye sabse zyada inquiries aa rahi hain? Main uske around ek targeted post draft kar sakti hoon."
        return f"Quick question{', ' + name if name else ''}: what service is getting the most inquiries this month? I'll draft a targeted post around it."

    if lang == "hi-en":
        return "Main aapka Google post already draft kar chuki hoon — sirf approval chahiye. Reply YES aur main publish kar deti hoon. STOP agar interested nahi."
    return "I've already drafted a Google post for you — just need your go-ahead. Reply YES to publish, STOP to skip."
