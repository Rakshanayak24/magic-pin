"""
conversation_handlers.py — Multi-turn Conversation State Machine (v3.0)
========================================================================
Fixes from v2.0:
  1. Auto-reply detection: detect BEFORE sending (exit after 2nd detection, not 3rd)
  2. STOP/hostile handling: detect "STOP", abuse, off-topic — respond once, then end
  3. Intent detection expanded: more Hindi patterns, STOP keyword
  4. Farewell messages are now included in the reply action body (not just rationale)
  5. Auto-reply hook is a strong curiosity hook (not generic)
"""
from __future__ import annotations

import re
import json
import logging
from dataclasses import dataclass, field
from typing import Optional
from datetime import datetime, timezone

log = logging.getLogger("vera.conv")


# ─── State Machine ────────────────────────────────────────────────────────────
class ConvState:
    QUALIFY   = "qualify"
    PITCH     = "pitch"
    ACTION    = "action"
    FOLLOW_UP = "follow_up"
    CLOSED    = "closed"


@dataclass
class ConversationState:
    conversation_id: str
    merchant_id: str
    customer_id: Optional[str] = None
    trigger_id: Optional[str] = None
    state: str = ConvState.PITCH
    history: list[dict] = field(default_factory=list)
    auto_reply_count: int = 0
    turn_count: int = 0
    detected_language: str = "en"
    last_vera_body: str = ""
    merchant_context: Optional[dict] = None
    category_context: Optional[dict] = None
    trigger_context: Optional[dict] = None


# ─── Language detection ───────────────────────────────────────────────────────
HINDI_MARKERS = [
    "haan", "nahi", "kya", "hai", "hoon", "karo", "shukriya",
    "bahut", "theek", "bilkul", "chalega", "zaroor", "mujhe",
    "aapka", "aapki", "mere", "main", "toh", "ki", "ka", "ke",
    "bhi", "agar", "abhi", "phir", "accha", "samajh",
]

def detect_language(message: str) -> str:
    ml = message.lower()
    count = sum(1 for w in HINDI_MARKERS if w in ml)
    return "hi-en" if count >= 2 else "en"


# ─── Auto-reply detection ─────────────────────────────────────────────────────
AUTO_REPLY_SIGNATURES = [
    "thank you for contacting",
    "thank you for reaching out",
    "thanks for contacting",
    "thanks for reaching out",
    "i am currently unavailable",
    "i am not available",
    "currently unavailable",
    "out of office",
    "automated message",
    "automated assistant",
    "automated reply",
    "this is an auto",
    "auto-generated",
    "autogenerated",
    "main ek automated",
    "aapki jaankari ke liye bahut-bahut shukriya",
    "team tak pahuncha",
    "team tak pahuncha deti hoon",
    "will get back to you soon",
    "will get back to you",
    "i will get back",
    "be in touch shortly",
    "business hours",
    "during business hours",
    "we are currently closed",
    "our team will respond",
    "someone will get back",
]

def detect_auto_reply(message: str, history: list[dict]) -> bool:
    """Detect WA Business canned auto-reply."""
    msg_lower = message.lower().strip()

    # Signature match
    for sig in AUTO_REPLY_SIGNATURES:
        if sig in msg_lower:
            return True

    # Verbatim repeat from merchant (same message as their previous message)
    prev_merchant_msgs = [
        t["msg"].strip() for t in history
        if t.get("from") in ("merchant", "customer")
    ]
    if len(prev_merchant_msgs) >= 1 and prev_merchant_msgs[-1] == message.strip():
        return True

    return False


# ─── Intent detection ─────────────────────────────────────────────────────────
ACCEPT_PATTERNS = [
    r"^(yes|yeah|yep|yup|sure|ok|okay|k|done|go|proceed)[.!?,\s]*$",
    r"^(haan|ha|ji haan|theek hai|theek h|chalega|bilkul|zaroor)[.!?,\s]*$",
    r"^yes[,\s]+(send|do|please|book|draft|go|proceed|start|share|show|register|confirm|activate|publish)",
    r"^(haan|ha)\s+(zaroor|bilkul|please|bhai|ji)",
    r"\b(go ahead|let'?s do it|karo|kar do|shuru karo|start karo)\b",
    r"\b(sign me up|join|register|sign up|judrna hai|joinaa hai)\b",
    r"\b(please proceed|please go ahead|yes please|please do it)\b",
    r"\b(sounds good|looks good|perfect|great|awesome|absolutely)\b",
    r"\b(zaroor|bilkul|haan ji|ji zaroor|haan bilkul)\b",
    r"^(send|book|confirm|draft|publish|activate|share|show me)[.!?\s]",
]

REJECT_PATTERNS = [
    r"\b(no|nope|nahi|na|not interested|interested nahi)\b",
    r"\bstop\b",
    r"\b(band karo|mat karo|rehne do|choddo)\b",
    r"\b(later|baad mein|abhi nahi)\b",
    r"\b(busy hoon|time nahi|leave me alone)\b",
    r"\b(unsubscribe|opt out|remove me|block)\b",
    r"\b(don'?t (want|need|care)|not now)\b",
]

HOSTILE_PATTERNS = [
    r"\b(idiot|stupid|useless|waste|scam|fraud|bakwaas|chutiya|gaali)\b",
    r"\b(get lost|go away|shut up|f[*u]ck|damn you)\b",
    r"(gst|income tax|legal|court|police|complaint)",  # off-topic / hostile redirect
]

def detect_intent(message: str) -> str:
    """Returns: accept | reject | hostile | question | neutral"""
    ml = message.lower().strip()

    for pat in HOSTILE_PATTERNS:
        if re.search(pat, ml, re.IGNORECASE):
            return "hostile"

    for pat in REJECT_PATTERNS:
        if re.search(pat, ml, re.IGNORECASE):
            return "reject"

    for pat in ACCEPT_PATTERNS:
        if re.search(pat, ml, re.IGNORECASE):
            return "accept"

    if "?" in message:
        return "question"

    return "neutral"


# ─── Main handler ─────────────────────────────────────────────────────────────
def respond(state: ConversationState, merchant_message: str) -> dict:
    """
    State machine handler for clear-intent messages.
    Called for: auto-reply, accept, reject, hostile.
    """
    # Update language detection
    detected_lang = detect_language(merchant_message)
    if detected_lang == "hi-en":
        state.detected_language = "hi-en"

    is_auto = detect_auto_reply(merchant_message, state.history)
    intent = detect_intent(merchant_message)

    # ── Auto-reply branch ─────────────────────────────────────────────────────
    if is_auto:
        state.auto_reply_count += 1

        if state.auto_reply_count >= 2:
            # ── CRITICAL FIX: Exit after 2nd auto-reply detection (send + end) ──
            farewell = _get_farewell(state, "auto_reply")
            _record_vera(state, farewell)
            state.state = ConvState.CLOSED
            return {
                "action": "end",
                "body": farewell,
                "cta": "none",
                "rationale": f"Auto-reply detected {state.auto_reply_count}× — exiting gracefully",
            }

        # First auto-reply → ONE direct hook (not a generic send)
        hook = _compose_auto_reply_hook(state)
        _record_vera(state, hook)
        return {
            "action": "send",
            "body": hook,
            "cta": "binary_yes_stop",
            "rationale": "Auto-reply detected (count=1) — sending one direct hook to reach real owner",
        }

    # ── Hostile branch ────────────────────────────────────────────────────────
    if intent == "hostile":
        lang = state.detected_language
        merchant = state.merchant_context or {}
        name = merchant.get("identity", {}).get("owner_first_name", "")
        if lang == "hi-en":
            body = f"Koi baat nahi{', ' + name if name else ''} — main yahan hoon jab bhi aap ready hon. 🙏"
        else:
            body = f"No problem{', ' + name if name else ''} — I'm here whenever you need me. Take care! 🙏"
        _record_vera(state, body)
        state.state = ConvState.CLOSED
        return {
            "action": "end",
            "body": body,
            "cta": "none",
            "rationale": "Hostile/off-topic message — exiting gracefully without escalating",
        }

    # ── Reject branch ─────────────────────────────────────────────────────────
    if intent == "reject":
        farewell = _get_farewell(state, "rejected")
        _record_vera(state, farewell)
        state.state = ConvState.CLOSED
        return {
            "action": "end",
            "body": farewell,
            "cta": "none",
            "rationale": "Merchant declined — graceful exit preserves relationship",
        }

    # ── Accept branch ─────────────────────────────────────────────────────────
    if intent == "accept":
        state.state = ConvState.ACTION
        action_msg = _compose_action_response(state, merchant_message)
        _record_vera(state, action_msg)
        return {
            "action": "send",
            "body": action_msg,
            "cta": "open_ended",
            "rationale": "Merchant accepted — transitioned to action mode immediately, no qualifying questions",
        }

    # Fallback for neutral (shouldn't reach here in fast-path, but safety net)
    advance_msg = _compose_advance(state, merchant_message)
    _record_vera(state, advance_msg)
    return {
        "action": "send",
        "body": advance_msg,
        "cta": "binary_yes_stop",
        "rationale": "Neutral response — advancing with new value angle",
    }


# ─── Helpers ──────────────────────────────────────────────────────────────────

def _record_vera(state: ConversationState, body: str):
    state.history.append({"from": "vera", "msg": body, "ts": datetime.now(timezone.utc).isoformat()})
    state.last_vera_body = body


def _get_farewell(state: ConversationState, reason: str) -> str:
    lang = state.detected_language
    merchant = state.merchant_context or {}
    name = merchant.get("identity", {}).get("owner_first_name", "")

    if reason == "auto_reply":
        if lang == "hi-en":
            return f"Koi baat nahi{', ' + name if name else ''} — samajh gayi. Owner se directly connect karti hoon. Best wishes! 🙂"
        return f"Got it{', ' + name if name else ''} — I'll connect with the owner/manager directly. Best wishes! 🙂"
    elif reason == "rejected":
        if lang == "hi-en":
            return f"Bilkul theek hai{', ' + name if name else ''} — jab bhi ready hon, main yahan hoon. 🙏"
        return f"Absolutely fine{', ' + name if name else ''} — whenever you're ready, I'm here. 🙏"
    else:
        if lang == "hi-en":
            return "Theek hai — baad mein baat karte hain. Good luck! 🙂"
        return "No worries — we'll catch up another time. Good luck! 🙂"


def _compose_auto_reply_hook(state: ConversationState) -> str:
    """Strong, data-specific hook to pierce auto-reply and reach the real owner."""
    merchant = state.merchant_context or {}
    category = state.category_context or {}
    lang = state.detected_language
    name = merchant.get("identity", {}).get("owner_first_name", "")
    perf = merchant.get("performance", {})
    peer_stats = category.get("peer_stats", {})
    customer_agg = merchant.get("customer_aggregate", {})
    cat_slug = category.get("slug", "")

    # Use the most compelling data point available
    ctr = perf.get("ctr", 0)
    peer_ctr = peer_stats.get("avg_ctr", 0.03)
    lapsed = customer_agg.get("lapsed_180d_plus", 0)

    prefix = f"Dr. {name}" if cat_slug in ("dentists",) and name else (name or "Hi")

    if lapsed > 10:
        if lang == "hi-en":
            return (
                f"{prefix}, {lapsed} patients 6+ months se wapas nahi aaye — "
                f"main unhe recall message bhej sakti hoon aapki taraf se. "
                f"Typically 20-30% wapas aate hain. Reply YES?"
            )
        return (
            f"{prefix}, {lapsed} of your patients haven't returned in 6+ months — "
            f"I can send them a recall message on your behalf. "
            f"Typically 20-30% come back. Reply YES?"
        )

    if ctr and peer_ctr and ctr < peer_ctr:
        missed = int(perf.get("views", 1000) * (peer_ctr - ctr))
        if lang == "hi-en":
            return (
                f"{prefix}, aapka CTR {int(ctr*100)}% hai vs peers ka {int(peer_ctr*100)}% — "
                f"iska matlab {missed} extra clicks/month chhoot rahe hain. "
                f"Main iska reason diagnose kar sakti hoon. Reply YES?"
            )
        return (
            f"{prefix}, your CTR is {int(ctr*100)}% vs peer avg {int(peer_ctr*100)}% — "
            f"that gap = ~{missed} missed visits/month. "
            f"I can fix this in 10 minutes. Reply YES?"
        )

    # Generic curiosity hook
    if lang == "hi-en":
        return f"{prefix}, ek quick question — is mahine sabse zyada kaunsi service ki demand aa rahi hai? (1 word bhi chalega)"
    return f"{prefix}, quick question — what's your most requested service this month? Even one word helps."


